package basico_de_sintaxe;

public class variaveis_seus_valores {
    public static void main(String[] args) {
        
        // variaveis e seus valores//
        int n = 4;
        String d = "igor";
        float f = 3.14f;
        boolean j = true;
        byte b = 100;

        // impressao das variaves e seus valores//
        System.out.printf(
            "\nO valor da string é %s\n\nvalor da inteiro é igual = %d, \n\nvalor de boolean é igual = %b, \n\nvalor do byte é igual = %d, \n\nvalor de float é de %f \n\n",
            d, n, j, b, f);
    }
}
